def to_nodal(q, k, fo, BW, R=None, L=None):
    with np.errstate(divide='ignore'):
        N = len(k) + 1
        wo = 2 * np.pi * fo
        QL = fo / BW
        QE = np.array(q) * QL
        K = np.array(k) / QL

        # find L0, C0 and RE
        if L is not None:
            L0 = np.ones(N) * L
            RE = wo * L0[::N-1] * QE
        elif R is not None:
            RE = np.ones(2) * R
            L0 = RE / (wo * QE)
            L0 = np.insert(L0, 1, np.ones(N-2) * L0[0])
        C0 = -1 / (wo**2 * L0)

        # find CK and C0 using K inverter
        Z = 1 / (wo * np.sqrt(C0[:-1] * C0[1:]))
        CK = -K / (wo * Z)
        CK = np.insert(np.zeros(2), 1, CK)
        C0 = C0 - CK[:-1] - CK[1:]

        # result
        CS = np.zeros(2 * N - 1)
        CP = np.zeros(2 * N - 1)
        LP = np.zeros(2 * N - 1)
        CS[1::2] = CK[1:-1]
        LP[0::2] = L0
        CP[0::2] = C0
        return [CS], [LP, CP], RE

def to_mesh(q, k, fo, BW, R=None, L=None, XM=None):
    with np.errstate(divide='ignore'):
        N = len(k) + 1
        wo = 2 * np.pi * fo
        QL = fo / BW
        QE = np.array(q) * QL
        K = np.array(k) / QL

        # find L0, C0 and RE
        if L is not None:
            L0 = np.ones(N) * L
            RE = wo * L0[::N-1] / QE
        elif R is not None:
            RE = np.ones(2) * R
            L0 = RE * QE / wo
            L0 = np.insert(L0, 1, np.ones(N-2) * L0[0])

        # find CK and C0 using K inverter
        Z = wo * np.sqrt(L0[:-1] * L0[1:])
        CK = -1 / (wo * K * Z)
        CK = np.insert(np.ones(2) * -np.inf, 1, CK)

        if XM is None:
            C0 = 1 / (-wo**2 * L0 - 1 / CK[:-1] - 1 / CK[1:])
        else:
            C0 = -1 / (wo * XM.imag + 1 / CK[:-1] + 1 / CK[1:])

        CS = np.zeros(2 * N - 1)
        LS = np.zeros(2 * N - 1)
        CP = np.zeros(2 * N - 1)

        LS[0::2] = L0
        CS[0::2] = C0
        CP[1::2] = CK[1:-1]
        return [LS, CS], [CP], RE


def to_crystal_mesh(q, k, fo, BW, LM, CP=0, QU=np.inf):

    def func(fo):
        L = to_leff(fo, CM, LM, CP, QU) 
        XM = to_xeff(fo, CM, LM, CP, QU) 
        XS, XP, RE = to_mesh(q, k, fo, BW, L=L, XM=XM)
        CS = XS[-1]
        return XS, XP, RE, np.max(CS)

    wo = 2 * np.pi * fo
    LM = np.ones(len(k) + 1) * LM
    CM = 1 / (wo**2 * LM)

    fp = to_fp(fo, CM, LM, CP or 5e-12)
    fd = bisect(func, np.min(fo), np.max(fp))
    XS, XP, RE, _ = func(fd)

    XS.insert(0, np.zeros_like(XS[0]))
    XS[0][0::2] = -CM
    XS[1][0::2] = LM
    return XS, XP, RE, fd


# helper routines
