def to_lowpass(g, fo, R):
    g = np.array(g)
    wo = 2 * np.pi * fo
    LS = g[1:-1] * R / wo
    CP = -g[1:-1] / (wo * R)
    RE = g[[0,-1]] * R
    return LS, CP, RE

def to_highpass(g, fo, R):
    g = np.array(g)
    wo = 2 * np.pi * fo
    CS = -1 / (g[1:-1] * wo * R)
    LP = R / (g[1:-1] * wo)
    RE = g[[0,-1]] * R
    return CS, LP, RE

def to_bandpass(g, fo, BW, R):
    QL = fo / BW 
    LS, CP, RE = to_lowpass(g, fo / QL, R)
    CS, LP, RE = to_highpass(g, fo * QL, R)
    return [ LS, CS ], [ LP, CP ], RE

def to_bandstop(g, fo, BW, R):
    QL = fo / BW 
    LS, CP, RE = to_lowpass(g, fo * QL, R)
    CS, LP, RE = to_highpass(g, fo / QL, R)
    return [ LS, CS ], [ LP, CP ], RE

